Serialization là việc chuyển đổi dữ liệu quan trọng của một object vào một chuỗi kí tự và lưu nó ra tập tin. Khi cần thiết thì chỉ cần đọc các tập tin, phân tích các chuỗi và tạo lại các đối tượng.
Có 2 dạng:
Dạng 1: serialize với định dạng mà con người có thể đọc hiểu(YAML, JSON, CSV)
Dạng 2: serialize với định dạng mà con người không hiểu được(định dạng nhị phân-Marshal)
